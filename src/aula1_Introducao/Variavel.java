public class Variavel {

	public static void main(String[] args) {
		
		
		String nome = "Daniele"; //String tipo de texto
		int idade = 26; //int - variavel com o tipo inteiro
		boolean casado = true; //boolean - retorna true/false
		
		System.out.println("Nome: " + nome + " Idade: " + idade + " Casado: " + casado);
		
		/* tambem é possivel criar variaveis sem valor.
		String nome;	
		int idade;
		boolean casado;
		*/
		
		/* e caso tenha variaveis com o mesmo tipo é possivel declarar todas juntas.
		String nome, segundoNome, ultimoNome; 
		nome = "Daniele";
		segundoNome = "Barbosa da";
		ultimoNome = "Silva";	
		*/
		
		/* Nomenclatura 
		 * as variaveis sempre devem iniciar com letra minuscula
		 * nao pode iniciar uma variavel com numeros e nem pode separar o nome das variaveis com espaço
		 * */
		
		
	}
}
